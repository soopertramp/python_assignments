""" Question - 3 : Write a Python program to find the area of triangle """

base = float(input("Enter the base of the triangle: "))
height = float(input("Enter the height of the triangle: "))

area = 0.5 * base * height

print(f'The area of the triangle with {base} and {height} is {area}')